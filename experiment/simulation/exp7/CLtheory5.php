"Parts of speech" are the basic lexical categories of the words present in a language. It is very important to understand these, in order to understand the grammar and use of the language concerned. <br/><br/>
Eg:<ul>
<li>Ram(Noun) ate(Verb) an(Determiner) apple(Noun) with(Preposition) a(Article) fork(Noun).</li>
<li>राम(Noun) ने(Postposition) चम्मच(Noun) से(Postposition) मिठाई(Noun) खाई(Verb).</li></ul>
<br/>
Each part of speech explains how the word is used. In fact, the same word can act as different parts of speechs depending on the context. <br/><br/>

Eg: A noun in one sentence and an adjective in the next. <br/>

<ul> <li>The glass was broken(Verb). </li>
         <li>The broken(Adjective) glass pierced her flesh. </li>
<br/><br/>

Part-of-speech tagging is the process of assigning a part-of-speech (lexical class marker) to each word in a corpus. <br/><br/>

